@dataclass(frozen=True)
class ToyState:
    A: int
    sigma: int
    tau: int


def predict_trace_parity(s: ToyState) -> dict[tuple[str, ...], int]:
    out = {}

    if s.A == 0:
        out[("A","O","O","O")]     = s.tau
        out[("A","E1","E1","E1")]  = s.sigma
        out[("A","E2","E2","E2")]  = 0
    else:
        out[("A","E2","E2","E2")]  = 1
        out[("A","E1","E1","E1")]  = 1 if (s.sigma, s.tau) in {(0,0), (0,1)} else 0
        out[("A","O","O","O")]     = 1 if (s.sigma, s.tau) in {(0,0), (1,0)} else 0

    out[("E1","E1","E1","O")]      = 1 if (s.sigma, s.tau) in {(0,0), (1,1)} else 0
    out[("E1","E1","E1","E2")]     = s.sigma

    out[("D","E1","E1","E2")]      = 1
    out[("D","E1","E1","M+")]      = 1
    out[("D","E1","E2","E2")]      = 1
    out[("E2","E2","E2","E2")]     = 1

    out[("D","E1","O","M+")]       = 0
    out[("E1","E1","M+","M+")]     = 0
    out[("E1","E2","E2","E2")]     = 0
    out[("E1","E2","M+","M+")]     = 0
    out[("E1","M+","M+","O")]      = 0
    out[("E2","E2","E2","O")]      = 0

    return out


def tag(x: int) -> str:
    return "ODD" if x else "EVEN"


def main() -> None:
    print("\n====================")
    print("COMPARE TOY G60 V2 AGAINST FULL CUBE")
    print("====================\n")

    total = 0
    mismatches = 0
    for name, (A, sigma, tau) in STATES:
        pred = predict_trace_parity(ToyState(A, sigma, tau))
        state_fail = 0
        print(name)
        for trace in TRACES:
            p = pred[trace]
            m = MEASURED[name][trace]
            total += 1
            if p != m:
                mismatches += 1
                state_fail += 1
                print(f"  FAIL {trace}: predicted={tag(p)} measured={tag(m)}")
        if state_fail == 0:
            print("  PASS")
        print()

    print(f"total_checks = {total}")
    print(f"mismatches   = {mismatches}")
    print(f"matches      = {total - mismatches}")


if __name__ == "__main__":
    main()
